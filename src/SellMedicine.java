    public class MedicineTableHandler {

        private final Map<String, Medicine> medicineMap;
        private final List<Medicine> medicineList;
        private final JTable medicinesTable;

        public MedicineTableHandler(JTable medicinesTable) {
            this.medicineMap = new HashMap<>();
            this.medicineList = new ArrayList<>();
            this.medicinesTable = medicinesTable;
            loadMedicinesFromDatabase();
        }

        private void loadMedicinesFromDatabase() {
            try (Connection con = ConnectionProvider.getCon(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery("SELECT * FROM medicine")) {

                while (rs.next()) {
                    String uniqueId = rs.getString("uniqueId");
                    Medicine medicine = new Medicine(
                            uniqueId,
                            rs.getString("name"),
                            rs.getString("companyName"),
                            rs.getString("price")
                    );
                    medicineMap.put(uniqueId, medicine);
                    medicineList.add(medicine);
                }
                updateTableModel();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

        private void updateTableModel() {
            DefaultTableModel model = new DefaultTableModel(
                    new Object[]{"Unique ID", "Name", "Company", "Price"}, 0
            );
            for (Medicine medicine : medicineList) {
                model.addRow(new Object[]{
                    medicine.getUniqueId(),
                    medicine.getName(),
                    medicine.getCompanyName(),
                    medicine.getPrice()
                });
            }
            medicinesTable.setModel(model);
        }

        public void handleMouseClick(MouseEvent evt) {
            int index = medicinesTable.getSelectedRow();
            if (index >= 0 && index < medicineList.size()) {
                Medicine medicine = medicineList.get(index);
                updateUIWithMedicineDetails(medicine);
            }
        }

        private void updateUIWithMedicineDetails(Medicine medicine) {
            txtUniqueId.setText(medicine.getUniqueId());
            txtName.setText(medicine.getName());
            txtCompanyName.setText(medicine.getCompanyName());
            txtPricePerUnit.setText(medicine.getPrice());
            txtNoOfUnits.setText("");
            txtTotalPrice.setText("");
        }

        // Inner class to represent a Medicine
        private static class Medicine {

            private final String uniqueId;
            private final String name;
            private final String companyName;
            private final String price;

            public Medicine(String uniqueId, String name, String companyName, String price) {
                this.uniqueId = uniqueId;
                this.name = name;
                this.companyName = companyName;
                this.price = price;
            }

            // Getters
            public String getUniqueId() {
                return uniqueId;
            }

            public String getName() {
                return name;
            }

            public String getCompanyName() {
                return companyName;
            }

            public String getPrice() {
                return price;
            }
        }
    }

