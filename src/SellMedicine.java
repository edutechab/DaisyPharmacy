    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (finalTotalPrice != 0) {
            billId = getUniqueId("Bill-");

            DefaultTableModel dtm = (DefaultTableModel) cartTable.getModel();
            if (cartTable.getRowCount() != 0) {
                for (int i = 0; i < cartTable.getRowCount(); i++) {
                    try {
                        Connection con = ConnectionProvider.getCon();
                        PreparedStatement ps = con.prepareStatement("update medicine set quantity=quantity-? where uniqueId=?");
                        ps.setInt(1, Integer.parseInt(dtm.getValueAt(i, 3).toString().trim())); // No. of Units
                        ps.setString(2, dtm.getValueAt(i, 0).toString().trim()); // Medicine ID
                        ps.executeUpdate();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Error updating quantity: " + e.getMessage());
                    }
                }
            }

            try {
                SimpleDateFormat myFormat = new SimpleDateFormat("dd-MM-yyyy");
                Calendar cal = Calendar.getInstance();
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into bill(billId,billDate,totalPaid,generatedBy) values(?,?,?,?)");
                ps.setString(1, billId);
                ps.setString(2, myFormat.format(cal.getTime()));
                ps.setInt(3, finalTotalPrice);
                ps.setString(4, username);
                ps.executeUpdate();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            for (int i = 0; i < cartTable.getRowCount(); i++) {
                for (int j = 0; j < cartTable.getColumnCount(); j++) {
                    System.out.print(cartTable.getValueAt(i, j) + " | ");
                }
                System.out.println();
            }

            //Create Bill document
            // Inside the jButton3ActionPerformed method, replace the existing PDF generation code with this:
            com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
            try {
                PdfWriter writer = PdfWriter.getInstance(doc, new FileOutputStream(PharmacyUtils.billPath + "" + billId + ".pdf"));
                doc.open();
                Paragraph pharmacyName = new Paragraph("                                                DAISY PHARMACY - OMANJOR,ACCRA\n");
                doc.add(pharmacyName);
                Paragraph hyphenLine = new Paragraph("----------------------------------------------------------------------------------------------------------------------------------");
                doc.add(hyphenLine);
                Paragraph details = new Paragraph("\tBILL ID: " + billId + "\n DATE: " + new Date() + "\n TOTAL PAID[Ghc]: " + finalTotalPrice);
                doc.add(details);
                doc.add(hyphenLine);
                PdfPTable tbl = new PdfPTable(5);
                tbl.setWidthPercentage(100);
                tbl.setSpacingBefore(10f);
                tbl.setSpacingAfter(10f);

                // Set column widths
                float[] columnWidths = {1.5f, 1.5f, 1.5f, 1.5f, 1f};
                tbl.setWidths(columnWidths);

                // Add table headers
                String[] headers = {"MEDICINE ID", "NAME", "PRICE PER UNIT", "QUANTITY", "SUB TOTAL PRICE"};
                for (String header : headers) {
                    PdfPCell cell = new PdfPCell(new Phrase(header, new Font(Font.FontFamily.HELVETICA, 10, Font.BOLD)));
                    cell.setHorizontalAlignment(Element.ALIGN_CENTER);
                    cell.setPadding(5);
                    tbl.addCell(cell);
                }

                // Add data rows
                DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
                System.out.println("Number of rows in cart: " + model.getRowCount());
                for (int i = 0; i < model.getRowCount(); i++) {
                    for (int j = 0; j < 5; j++) {
                        Object value = model.getValueAt(i, j);
                        String cellValue = value != null ? value.toString() : "";
                        System.out.println("Adding cell value: " + cellValue);
                        PdfPCell cell = new PdfPCell(new Phrase(cellValue, new Font(Font.FontFamily.HELVETICA, 9, Font.NORMAL)));
                        cell.setHorizontalAlignment(Element.ALIGN_CENTER);
                        cell.setPadding(5);
                        tbl.addCell(cell);
                    }
                }

                doc.add(tbl);
                doc.add(hyphenLine);
                Paragraph thanksMsg = new Paragraph("Thank you for your purchase. Please Visit again. We are open 24/7");
                doc.add(thanksMsg);

                // Ensure all content is written to the PDF
                doc.close();
                writer.close();

                System.out.println("PDF generated successfully: " + PharmacyUtils.billPath + "" + billId + ".pdf");

                OpenPdf.openById(String.valueOf(billId));
                // OPEN PDF
                OpenPdf.openById(String.valueOf(billId));
            } catch (DocumentException | FileNotFoundException e) {
                JOptionPane.showMessageDialog(null, "Error generating PDF: " + e.getMessage());
            }

            doc.close();

        } else {
            JOptionPane.showMessageDialog(null, "Please add some Medicine to Cart");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

